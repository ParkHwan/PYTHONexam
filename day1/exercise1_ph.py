number1 = int(input("숫자1 입력: "))
number2 = int(input("숫자2 입력: "))
sum = number1 + number2
mul = number1 * number2
up = number1 // number2
down = number1 % number2
print(number1, "+", number2, "=", sum)
print(number1, "*", number2, "=", mul)
print(number1, "//", number2, "=", up)
print(number1, "%", number2, "=", down)
